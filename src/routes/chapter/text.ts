莉莉随口应了一句，而这时，面团已经揉好了。由于糖的颜色，面团呈现浅棕色。
“接下来，只要把面团搓成细细的长条，然后切成一小块一小块的，搓成圆形的丸子。”
“圆形……因为是珍珠吗？”基兰帕终于找到了时机开口。
这次莉莉的回答却是有些暧昧的：“嗯……大概是吧。”
“大，大概……？”
基兰帕感觉他每次开口都能踩在这个女人的雷点上。
这时，丝丝尔也正好从楼上下来，帮着莉莉一起搓丸子。
两人将搓好的丸子都裹上了一层“淀粉”。丝丝尔转过头向四人解释：“这样可以防止黏在一起喵！”
“该煮了。”
莉莉提醒她。
“哦哦喵！”
这次，莉莉又在400克水中加入了80克糖。水烧开后，她将刚才搓好的丸子一股脑倒了进去。
“这里的糖可以根据自己的喜欢的甜度进行调节。”
莉莉似乎是真心想教会四人“珍珠”的做法。
繁忙的工作似乎进入了尾声，不久前手臂都能挥出残影的莉莉，此刻悠闲地站在灶前，时不时搅拌一下锅里的丸子。
“是约书亚先生吗？刚才问我为什么这么大方向你们展示做法的。”
“是的！”骤然被点名，约书亚像是学校里的学生那样应声道。
“可能你们没法理解。”
莉莉难得转过头，看向吧台外边趴着的四个男人。她的脸上依旧没什么表情，只是多了那么一些……思念，还有无可奈何。
“对我来说，看到这个世界的更多人喜欢上这种食物——能够对我的内心有一些安慰。”
基兰帕感觉这是一句十分意味深长的话。
不过，比起思索话语背后的含义，他最先想到的反而是7年前，女神通过启示赐予自己“圣骑士”天职的那一天。
他其实并不想成为圣骑士。这件事，和他从小就在一起的加兰斯、约书亚知道，长大后一起训练的艾斯特斯也知道，教会里所有认识他的人都知道。
但……他是女神的代行使者。他需要作为女神的圣骑士，为女神清扫迷宫里那些因为魔界的影响滋生的可悲魔物。
基兰帕莫名能和莉莉周身萦绕着的那层淡淡的无可奈何共情。
……其实他们两人，并不是那么的气场不和吧？
就在他这么想时，丝丝尔清脆的嗓音打断了他的思路：“前辈，好像差不多煮好了喵！”
莉莉闻言转过头，用筛子将锅里的丸子给捞了出来——不，现在不应该用“丸子”这样笨重的词语去形容了。那些被捞起的球状物已变得晶莹、圆润，甚至有些半透明，被倒进碗里时还会微微颤动。
就像是真正的珍珠那样。
莉莉转过身，她的手臂带起一阵风，将碗举到四人面前。
“这就是珍珠。”
“这就是……珍珠。”
第一个出声的居然是艾斯特斯：“……这是魔药吗？难道是在熬制的过程中发生了什么……”
“哇！”加兰斯则是十分捧场的发出了惊呼，“没想到成品这么漂亮，吃起来是什么味道啊？”
“这个不是直接吃啦喵！”
一旁的丝丝尔活用兽人族的灵活身体，一次性端来了四杯奶茶放在四人的面前。
“珍珠，要加进这里面喵。”

 
05

四个简约的玻璃杯里装了浅棕色的奶茶，是方才丝丝尔用红茶和牛奶煮的。
透过玻璃杯可以看见，杯底有一颗一颗的半透明球状物，这便是“珍珠”了。
加兰斯端着杯子左瞧右瞧，有些不敢相信的问：“这就是成品了？这么简单？”
“嗯嗯，是的喵！”
丝丝尔笑眯眯地又朝四人递上一物——一根中空的、似乎也是用迷宫里的收获制成的管状物。
“直接喝的话，是无法享受‘珍珠奶茶’的真正魅力的喵！请使用吸管喵！”
“吸管？”
虽然是第一次听到这个单词，但神奇的，基兰帕拿到这根管状物的瞬间，就知道应该怎么使用了。
——因为无论是人类、兽人、还是精灵，在婴孩时期都是通过吮吸的动作来获得食物的。
将吸管插入杯内，自然地吮吸了一口，基兰帕尝到了奶茶的醇香与甜味，但这只是最初的口感。伴随着奶茶被吸上来的，还有一颗颗方才刚刚煮好的，弹性十足的珍珠。
基兰帕下意识地嚼了嚼，眼睛一下子睁大了。
虽然加了糖，但珍珠实际上是不甜的，不如说几乎没有什么味道。可它Q弹的口感，为这杯奶茶增添了一丝趣味性。
一点小小的改变，却让奶茶这种平凡的饮品一下子变得新奇了起来。
该说是喝呢？还是吃呢？两个矛盾的概念糅合在了一起，却并不显得怪异，甚至令人感觉，奶茶这种饮品天生就该如此。
一旁的加兰斯和约书亚早就开始大呼小叫起来。前者是为了“珍珠奶茶”奇妙的口感，后者则是在向莉莉反复确认：“我们……我们学会珍珠的做法真的没有关系吗？我敢肯定，如果它传到王都，一定可以赚很多钱的……”
至于艾斯特斯，他正在默默复习珍珠的做法，似乎还没放弃“珍珠=魔药”的设想。
“我虽然开了这家店，但并没有要建立什么商业帝国的想法。”
莉莉再次向约书亚重申了自己的意愿。接着，她的语气里又带了点自豪。
“而且……不就是珍珠嘛。和我知道的更多配方相比，它只能算迷宫第一层的史莱姆罢了。”
约书亚被她的发言折服了：“不愧是前辈……”
这时，莉莉看向了基兰帕——毕竟四位客人里，只有基兰帕至今还一言不发了。
“圣骑士先生，你有什么建议吗？”
她貌似已经认定了这杯奶茶不对圣基兰帕的胃口，但又有些不服气。
思索了一会儿，基兰帕抬起头。
“……嗯，我很喜欢。”
他的眼眸是金色的，眼角的轮廓微微上挑，很容易让人觉得他无时无刻都带着微微的笑意，显得有些轻佻。
但此时，黑发金眼的圣骑士，眼神十分真诚：“实不相瞒，我并不讨厌甜食，但太多甜点都让我觉得过甜了。甜品师们告诉我，蛋糕里的糖是不能减的，因为那会让蛋糕无法成形。”
“……这，这样。”
莉莉眨了眨眼睛，不太明白基兰帕这段话的用意。
基兰帕抬起玻璃杯，手臂与身体连接处的盔甲轻微作响：“这杯奶茶加入了‘珍珠’后，比起一般的饮品，更多了甜点的感觉，尝起来又并没有那么甜。所以，我很喜欢。”
很认真，但是……很生硬。
一旁的加兰斯悄声吐槽：“基兰帕还是这样……这叫人怎么接话啊……”
和热情的剑士预测的那样，莉莉再度眨了眨眼睛，不知如何回答。
她只能礼貌回应：“谢谢你这么认真的点评……谢谢你。我很开心。”
她似乎依旧不太能和基兰帕顺畅交流。但不可否认的，被这样认真的夸奖之后，她的表情很是明朗了些。
将莉莉的表情变化看在眼里的基兰帕立刻再接再厉：
“说起来，莉莉小姐真的是非常有想法的人呢。”
“是，是吗？基兰帕先生不用这么夸奖我……”
“之前我就在想，‘木头土豆’是一个特别好的名字。”
“诶？！大家都说很搞笑……”
“怎么会？我觉得挺好的，”两人终于有一来一回的对话了！基兰帕桌子下的那只手暗暗握拳，“成熟期的这种魔物有着木头纹路的身体，树枝一样的分支上有着像土豆一样的‘果实’，所以叫‘木头土豆’，不是很有道理吗？我觉得又形象又直白，是个很好的名字！”
然而，此话一出，奶茶店里便陷入了死一般的沉寂。
莉莉的面庞变得僵硬，丝丝尔瞪大了眼睛，剩下三位同伴也用惊恐的眼神看向基兰帕。
“木头的身体，土豆的果实……”加兰斯不可思议地对基兰帕说，“我们见到的是同一种魔物？！”
约书亚也失声惊呼：“土豆？那不是会像心脏一样收缩跳动的瘤状物吗？？你是怎么看成土豆的？？”
艾斯特斯：“呃……嗯……”
丝丝尔三角形的猫耳慢慢压平：“基兰帕先生，品味好可怕……”
最后，是莉莉缓慢而尴尬的回答：
“抱，抱歉，我并不是因为这种理由，才给它取名叫‘木头土豆’的……”
啊。
基兰帕无声地感叹。
原来不是自己和莉莉的气场不合……而是他作为女神的代行使者，教会在冒险者群体中的延伸，还有很多方面需要学习。
